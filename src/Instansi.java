public class Instansi {
    String pilihInstansi;
    String provinsi;
    String kota;
    String kelurahan;
    String alamat;

    public Instansi(String pilihInstansi, String provinsi, String kota, String kelurahan, String alamat) {
        this.pilihInstansi = pilihInstansi;
        this.provinsi = provinsi;
        this.kota = kota;
        this.kelurahan = kelurahan;
        this.alamat = alamat;

    }
}
