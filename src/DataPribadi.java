public class DataPribadi {
     String namaLengkap;
     String email;
     int noTelp;
     String tempatLahir;
     String status;
     String tanggalLahir;

     public DataPribadi(String namaLengkap, String email, int noTelp, String tempatLahir, String status, String tanggalLahir) {
        this.namaLengkap = namaLengkap;
        this.email = email;
        this.noTelp = noTelp;
        this.tempatLahir = tempatLahir;
        this.status = status;
        this.tanggalLahir = tanggalLahir;
    }
}

